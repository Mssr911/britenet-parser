DROP TABLE IF EXISTS CONTACTS;

CREATE TABLE CONTACTS (
    id INT AUTO_INCREMENT  PRIMARY KEY,
    id_customer INT NOT NULL,
    type INT NOT NULL,
    contact VARCHAR(50)
);